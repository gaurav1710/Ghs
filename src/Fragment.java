
public class Fragment {
	//Root of this MST fragment
	private Node root;

	//Level of this fragment
	private int level;
	
	public Node getRoot() {
		return root;
	}

	public void setRoot(Node root) {
		this.root = root;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	
}
